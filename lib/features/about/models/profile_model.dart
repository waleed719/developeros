class DeveloperProfile {
  final String name;
  final String title;
  final String bio;
  final String avatarUrl;
  final String email;
  final String githubUrl;
  final List<String> highlights;

  DeveloperProfile({
    required this.name,
    required this.title,
    required this.bio,
    required this.avatarUrl,
    required this.email,
    required this.githubUrl,
    required this.highlights,
  });
}
